"""
Дана строка, содержащая слова длиной не менее 4 символов, разделённые произвольным количеством пробелов.

Требуется:

1. Найти все пары соседних слов, в которых оба слова являются палиндромами
(то есть читаются одинаково слева направо и справа налево; сравнение выполняется без учёта регистра).
2. Из каждой такой пары взять оба слова, привести их к нижнему регистру и добавить в результат.
3. Удалить дубликаты, сохранив порядок первого появления каждого слова.
4. Отсортировать полученные уникальные слова в обратном алфавитном порядке (от 'z' к 'a', лексикографически).
5. Вывести слова через запятую с пробелом: ", ".

Пример 1
Ввод: 
```
radar level abba   deed  hello racecar   noon
```
Вывод:
```
radar, racecar, noon, level, deed, abba
```

Пример 2
Ввод: 
```
radar civic radar level civic abba radar civic
```

Вывод:
```
radar, level, civic, abba
```
"""

s = input().strip()
words = s.split()

collected = []

# Проверяем каждую пару соседних слов
for i in range(len(words) - 1):
    w1 = words[i].lower()
    w2 = words[i + 1].lower()
    if w1 == w1[::-1] and w2 == w2[::-1]:
        collected.append(w1)
        collected.append(w2)

# Убираем дубликаты, сохраняя порядок первого появления
seen = set()
unique = []
for w in collected:
    if w not in seen:
        unique.append(w)
        seen.add(w)

# Сортируем в обратном алфавитном порядке
unique.sort(reverse=True)

print(", ".join(unique))