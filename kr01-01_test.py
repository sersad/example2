"""
Напишите программу, которая выбирает строки, удовлетворяющие всем следующим условиям:

1. Последний символ — либо '!', либо '?'.
2. Во всей строке, кроме последнего символа, допускаются только буквы и пробелы (никаких цифр, знаков препинания, тире, кавычек и т.п.).
3. Только первое слово начинается с заглавной буквы, все остальные слова — строго строчные.
4. Длина всей строки кратна 2 или 7.
Ввод: сначала целое число n — количество строк, затем n строк.
Вывод: подходящие строки в том же порядке, по одной на строку.

Пример 1
Ввод:
```
5
Hello, world?
No way!
she starts lowercase!
Magic!
Just do it?
```
Вывод:
```
No way!
Just do it?
```
Пример 2
Ввод:
```
10
Run run!
Go cat!
Big dog?
A!
Hi?
ok!
A b!
A B?
!
Hello!
```
Вывод:
```
Run run!
Go cat!
Big dog?
A!
A b!
Hello!
```
"""

n = int(input())  # Читаем количество строк

for _ in range(n):
    s = input()  # Читаем очередную строку

    # 1. Строка должна быть непустой и заканчиваться на '!' или '?'
    if len(s) == 0:
        continue
    if s[-1] not in '!?':
        continue

    # 2. Отделяем "тело" строки — всё, кроме последнего символа
    body = s[:-1]

    # Если тело пустое (например, строка "!"), то слов нет — пропускаем
    if not body.strip():
        continue

    # 3. Проверяем: в теле разрешены ТОЛЬКО буквы и пробелы
    if not all(char.isalpha() or char.isspace() for char in body):
        continue

    # 4. Разбиваем тело на слова (удаляя возможные лишние пробелы по краям и между словами)
    words = body.split()

    # Если после split() слов нет (например, строка "   !"), пропускаем
    if not words:
        continue

    # 5. Проверка заглавных букв:
    #    - первое слово должно быть в формате Title (например, "Hello", но не "HELLO" или "hello")
    #    - все остальные слова должны быть строго строчными (например, "world", но не "World")
    if not words[0].istitle():
        continue
    if not all(word.islower() for word in words[1:]):
        continue

    # 6. Проверка длины строки: должна делиться на 2 ИЛИ на 7
    if len(s) % 2 == 0 or len(s) % 7 == 0:
        print(s)